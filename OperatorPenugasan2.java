public class OperatorPenugasan2 {
    public static void main(String[] args) {
        int angka1, angka2;

        angka1=10;
        angka2=20;

        angka1+=angka2;
        System.out.println(angka1);
        angka1-=angka2;
        System.out.println(angka1);
        angka1*=angka2;
        System.out.println(angka1);
        angka1/=angka2;
        System.out.println(angka1);
    }
    
}
