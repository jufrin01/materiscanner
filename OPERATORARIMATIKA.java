public class OPERATORARIMATIKA {
    public static void main(String[] args) {
        double a,b,c,d,e,nilai;
        //TIPE DATA ARIMATIKA BISA PAKAI INTEGER,DOUBLE,BYTE,SHORT,MAUPUN LONG;

        a = 10;
        b = 20;
        c = 30;
        d = 40;
        e = 50;
        //SIMBOL ARITMATIKA MELIPUTI;
        // TANDA + PENJUMLAHAN
        // TANDA - PENGURANGAN
        // TANDA * PERKALIAN
        // TANDA / PEMBAGIAN
        // TANDA % SISA HASIL


        nilai = a+b+c+d+e;
        System.out.println("NILAI HASIL PENJUMLAHAN  = "+nilai);
        nilai = a-b-c-d-e;
        System.out.println("NILAI HASIL PENGURANGAN  = "+nilai);
        nilai = a*b*c*d*e;
        System.out.println("NILAI HASIL PERKALIAN    = "+nilai);
        nilai = a/b/c/d/e;
        System.out.println("NILAI HASIL PENBAGIAN    = "+nilai);
        nilai = a%b%c%d%e;
        System.out.println("NILAI HASIL SISA HASIL   = "+nilai);

    }
    
}
